arr = [5, 2, 6, 1, 9, 3, 7, 8, 4]
def selection_sort(N, arr):
    for i in range(N-1, 0, -1):
        for j in range(i):
            if arr[j] > arr[j+1]:
                arr[j], arr[j+1] = arr[j+1], arr[j]
    return arr

